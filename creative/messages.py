# ==================== 메시지 생성 함수 ====================

def get_creative_message(tool_name: str) -> str:
    """도구별 창의적인 메시지 반환
    
    Args:
        tool_name: 도구 이름
        
    Returns:
        랜덤하게 선택된 창의적인 메시지
    """
    messages = BREAK_MESSAGES.get(tool_name, ["휴식 중..."])
    return random.choice(messages)


def get_stress_comment() -> str:
    """스트레스 해소 코멘트 반환"""
    return random.choice(STRESS_RELIEF_COMMENTS)


def get_boss_comment(boss_level: int) -> str:
    """Boss Alert Level에 따른 코멘트 반환"""
    return BOSS_ALERT_COMMENTS.get(boss_level, "")


def get_off_work_message() -> str:
    """퇴근 메시지 반환"""
    return random.choice(OFF_WORK_MESSAGES)


def get_return_to_work_message() -> str:
    """출근 메시지 반환"""
    return random.choice(RETURN_TO_WORK_MESSAGES)


def get_full_response_message(tool_name: str, boss_level: int) -> str:
    """전체 응답 메시지 생성 (메인 메시지 + Boss 코멘트)"""
    main_msg = get_creative_message(tool_name)
    boss_comment = get_boss_comment(boss_level)
    
    # Boss Alert Level이 1 이상일 때만 코멘트를 추가하여, 평온한 상태에서는 군더더기 없는 메시지를 출력
    if boss_level > 0 and boss_comment:
        return f"{main_msg}\n\n{boss_comment}"
    return main_msg

